extension EmojiPickerViewController {
    override func collectionView(_ collectionView: UICollectionView, didSelectItemAt indexPath: IndexPath) {
        let pickedEmoji = emoji[indexPath.item]
        emojiPickedHandler?(pickedEmoji)
    }
